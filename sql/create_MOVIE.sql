CREATE TABLE MOVIE (
Mov_Id INT NOT NULL AUTO_INCREMENT,
Mov_Name VARCHAR(30) NOT NULL,
Mov_Year INT NOT NULL,
Mov_Lang VARCHAR(15) NOT NULL,
Mov_Country VARCHAR(30) NOT NULL,
Mov_Age INT NOT NULL,
Mov_Dir INT NOT NULL,
Mov_Cast_1 INT NOT NULL,
Mov_Cast_2 INT,
Mov_Cast_3 INT,
Mov_Cast_4 INT,
Mov_Cast_5 INT,
Mov_Rate INT,
Mov_Des TEXT NOT NULL,
Mov_Type VARCHAR(15) NOT NULL,
Mov_Link TEXT NOT NULL,
PRIMARY KEY (Mov_Id),
FOREIGN KEY (Mov_Dir) REFERENCES DIRECTOR(Dir_Id),
FOREIGN KEY (Mov_Cast_1) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (Mov_Cast_2) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (Mov_Cast_3) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (Mov_Cast_4) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (Mov_Cast_5) REFERENCES CAST_TABLE(Ch_Id));