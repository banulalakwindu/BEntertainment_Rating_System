CREATE TABLE TV (
TV_Id INT NOT NULL AUTO_INCREMENT,
TV_Name VARCHAR(30) NOT NULL,
TV_FirstAir INT NOT NULL,
TV_Lang VARCHAR(15) NOT NULL,
TV_Country VARCHAR(30) NOT NULL,
TV_Age INT NOT NULL,
TV_NoSeason INT NOT NULL,
TV_NoEpisode INT NOT NULL,
TV_Cast_1 INT NOT NULL,
TV_Cast_2 INT,
TV_Cast_3 INT,
TV_Cast_4 INT,
TV_Cast_5 INT,
TV_Rate INT,
TV_Des TEXT NOT NULL,
TV_Type VARCHAR(15) NOT NULL,
TV_Link TEXT NOT NULL,
PRIMARY KEY (TV_Id),
FOREIGN KEY (TV_Cast_1) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (TV_Cast_2) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (TV_Cast_3) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (TV_Cast_4) REFERENCES CAST_TABLE(Ch_Id),
FOREIGN KEY (TV_Cast_5) REFERENCES CAST_TABLE(Ch_Id));