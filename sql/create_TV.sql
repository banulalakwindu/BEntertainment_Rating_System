CREATE TABLE `ratingdb`.`tv_show` (
  `TV_Id` INT NOT NULL AUTO_INCREMENT,
  `TV_Name` VARCHAR(55) NOT NULL,
  `TV_Lang` VARCHAR(55) NOT NULL,
  `TV_Country` VARCHAR(55) NOT NULL,
  `TV_Age` VARCHAR(10) NULL,
  `TV_FirstAir` INT NOT NULL,
  `TV_NoSeason` INT NOT NULL,
  `TV_NoEpisode` INT NOT NULL,
  `TV_Desc` TEXT NOT NULL,
  `TV_Link` TEXT NOT NULL,
  `TV_Type` VARCHAR(55) NOT NULL,
  `TV_Cast_1` INT NOT NULL,
  `TV_Cast_2` INT NULL,
  `TV_Cast_3` INT NULL,
  `TV_Dir` INT NOT NULL,
  `TV_Rate` INT NOT NULL,
  PRIMARY KEY (`TV_Id`));